#Quicksort
#Es un algoritmo de ordenamiento mucho mas rapido que el algoritmo de ordenamiento por seleccion.
#Quicksort se basa en una tecnica llamada Divide&Conquer (D&C)
#D&C es un algoritmo recursivo que se centra en separar en subproblemas similares al original.

#Nota: Consultar algoritmo euclidiano.

#CODE: Sumar todos los numeros y devolver el total
def sum(arr):
    total=0
    for x in arr:
        total+= x
    return total

print(sum(1,2,3,))
